//! ===================================== What is Node.js =====================================

// Node.js is an open-source, cross-platform runtime environment that allows JavaScript
// to be executed server-side. It is designed to build scalable network applications.

// Node.js was created by Ryan Dahl in 2009. It uses the V8 JavaScript engine developed by Google for Chrome.

// Node.js uses an event-driven, non-blocking I/O model, unlike traditional server-side
// technologies which are often multi-threaded and blocking.

// Example: Basic Node.js script
console.log("Hello, Node.js!");
